package com.upgrad.fsd;

public class Adder {

    public int addition(int a, int b) {
        return a + b;
    }

    public double addition(double a, double b) {
        return a + b;
    }

    public float addition(float a, float b) {
        return a + b;
    }

    public long addition(long a, long b) {
        return a + b;
    }
}
